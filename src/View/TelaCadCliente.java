    private void jButtonSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarClienteActionPerformed
        
        BeansCadCliente clienteAtual = new BeansCadCliente();
        
        try{
            clienteAtual.setNome(jFormattedTextFieldNomeCliente.getText());
            clienteAtual.setSobrenome(jFormattedTextFieldSobrenomeCliente.getText());
            clienteAtual.setCpf(jFormattedTextFieldCPFCliente.getText());
            clienteAtual.setRg(jFormattedTextFieldRGCliente.getText());
            clienteAtual.setEstado((String) jComboBoxEstado.getSelectedItem().toString());
            clienteAtual.setCidade((String) jComboBoxCidade.getSelectedItem().toString());
            clienteAtual.setEndereco(jFormattedTextFieldEnderecoCliente.getText());
            clienteAtual.setBairro(jTextFieldBairroCliente.getText());
            
            
            clienteAtual.setNumero(Integer.parseInt(jTextFieldNumeroCliente.getText()));
            
            clienteAtual.setCep(jTextFieldCEPCliente.getText());
            clienteAtual.setReferencia(jTextFieldReferenciaCliente.getText());
            clienteAtual.setTelefone(jFormattedTextFieldTelefoneCliente.getText());
            
            clienteAtual.setId_cidade(BeansCidade.buscarPorNome(jComboBoxCidade.getSelectedItem().toString(), jComboBoxEstado.getSelectedItem().toString()).getId());
            
            if(flag == EDITAR){
               
                clienteAtual.setId((Integer.parseInt(jTextFieldID.getText())));
                
            }
            clienteAtual.salvar();
            
            jTextFieldBairroCliente.setEnabled(false);
            jTextFieldBuscaCliente.setEnabled(false);
            jTextFieldCEPCliente.setEnabled(false);
            jTextFieldNumeroCliente.setEnabled(false);
            jTextFieldReferenciaCliente.setEnabled(false);
            jFormattedTextFieldCPFCliente.setEnabled(false);
            jFormattedTextFieldEnderecoCliente.setEnabled(false);
            jFormattedTextFieldNomeCliente.setEnabled(false);
            jFormattedTextFieldRGCliente.setEnabled(false);
            jFormattedTextFieldSobrenomeCliente.setEnabled(false);
            jFormattedTextFieldTelefoneCliente.setEnabled(false);
            jComboBoxEstado.setEnabled(false);
            jComboBoxCidade.setEnabled(false);
            jButtonSalvarCliente.setEnabled(false);
            jButtonNovoCliente.setEnabled(true);
            jButtonEditaCliente.setEnabled(false);
            jTextFieldBuscaCliente.setEnabled(true);
            
            
            //JOptionPane.showMessageDialog(null, "Dados salvos!");
            jButtonLimparActionPerformed(evt);
            
            preencherTabela("select * from cliente join cidade using (id_cidade) join estado using (id_estado) order by primeiro_nome;");
        
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Verifique os campos numéricos!");
        }catch(ModelException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarClienteActionPerformed

    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String [] {"Nome","Sobrenome", "RG", "CPF", "Cidade", "UF", "Telefone"};
        conexao.conexao();
            conexao.executaSql(Sql);
            try{
               conexao.rs.first();
               do{
                   dados.add(new Object[]{conexao.rs.getString("primeiro_nome"), conexao.rs.getString("sobrenome"), conexao.rs.getString("rg"), conexao.rs.getString("cpf"), conexao.rs.getString("cidade.nome"), conexao.rs.getString("estado.uf"), conexao.rs.getString("telefone")});
                   
               }while(conexao.rs.next());
            }catch (SQLException ex){
                JOptionPane.showMessageDialog(rootPane,"Erro ao preencher tabela");
            }
            ModelBeans.BeansTabela modelo = new ModelBeans.BeansTabela(dados, colunas);
            jTableClientes.setModel(modelo);
            //PRIMEIRO NOME 
            jTableClientes.getColumnModel().getColumn(0).setPreferredWidth(135);
            jTableClientes.getColumnModel().getColumn(0).setResizable(false);
            //SOBRENOME
            jTableClientes.getColumnModel().getColumn(1).setPreferredWidth(105);
            jTableClientes.getColumnModel().getColumn(1).setResizable(false);
            //RG
            jTableClientes.getColumnModel().getColumn(2).setPreferredWidth(115);
            jTableClientes.getColumnModel().getColumn(2).setResizable(false);
            //CPF
            jTableClientes.getColumnModel().getColumn(3).setPreferredWidth(115);
            jTableClientes.getColumnModel().getColumn(3).setResizable(false);
            //NOME DA CIDADE
            jTableClientes.getColumnModel().getColumn(4).setPreferredWidth(150);
            jTableClientes.getColumnModel().getColumn(4).setResizable(false);
            //NOME DO ESTADO
            jTableClientes.getColumnModel().getColumn(5).setPreferredWidth(50);
            jTableClientes.getColumnModel().getColumn(5).setResizable(false);
            //TELEFONE
            jTableClientes.getColumnModel().getColumn(6).setPreferredWidth(114);
            jTableClientes.getColumnModel().getColumn(6).setResizable(false);
            //NAO PODE REORDENAR O CABEÇALHO DA TABELA
            jTableClientes.getTableHeader().setReorderingAllowed(false);
            //NAO PODE SER REDIMENCIONADA 
            jTableClientes.setAutoResizeMode(jTableClientes.AUTO_RESIZE_OFF);
            //SO PODERA SELECIONAR UM DADO NA TABELA DE CADA VEZ 
            jTableClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conexao.desconecta();
    }
    
