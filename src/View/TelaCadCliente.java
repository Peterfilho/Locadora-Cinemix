    private void jTableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMouseClicked
        String nome =""+jTableClientes.getValueAt(jTableClientes.getSelectedRow(),0);
        //JOptionPane.showMessageDialog(rootPane, nome);
        conexao.conexao();
        conexao.executaSql("SELECT *, cidade.nome, estado.nome FROM cliente JOIN cidade USING ( id_cidade ) JOIN estado USING ( id_estado ) WHERE cliente.primeiro_nome LIKE '%"+nome+"%' order by primeiro_nome");
        
        try {
            if(conexao.rs.first()){
                jTextFieldID.setText(String.valueOf(conexao.rs.getInt("id_cliente")));
                jTextFieldBairroCliente.setText(conexao.rs.getString("bairro"));
                jTextFieldCEPCliente.setText(conexao.rs.getString("cep"));
                jTextFieldNumeroCliente.setText(String.valueOf(conexao.rs.getInt("numero")));
                jTextFieldReferenciaCliente.setText(conexao.rs.getString("referencia"));
                jFormattedTextFieldCPFCliente.setText(conexao.rs.getString("cpf"));
                jFormattedTextFieldEnderecoCliente.setText(conexao.rs.getString("rua"));
                jFormattedTextFieldNomeCliente.setText(conexao.rs.getString("primeiro_nome"));
                jFormattedTextFieldRGCliente.setText(conexao.rs.getString("rg"));
                jFormattedTextFieldSobrenomeCliente.setText(conexao.rs.getString("sobrenome"));
                jFormattedTextFieldTelefoneCliente.setText(conexao.rs.getString("telefone"));
                jComboBoxEstado.setSelectedItem(conexao.rs.getString("estado.nome"));
                jComboBoxCidade.setSelectedItem(conexao.rs.getString("cidade.nome"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar cliente "+ex);
            ex.printStackTrace();
        }    
        conexao.desconecta();
        jButtonEditaCliente.setEnabled(true);
        jButtonExcluirCliente.setEnabled(true);
        
    }//GEN-LAST:event_jTableClientesMouseClicked

