    public void preencherEstados(){    
        jComboBoxEstado.removeAllItems();
                
        for(BeansEstado be : BeansEstado.buscarTodos()){
            jComboBoxEstado.addItem(be.getNome());
        }
        
        jComboBoxEstado.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                preencherCidades();
            }
        });
        
    }
 
    public void preencherCidades(){
    
        jComboBoxCidade.removeAllItems();
        
        for(BeansCidade bc : BeansCidade.buscarTodos(jComboBoxEstado.getSelectedItem().toString())){
            
            jComboBoxCidade.addItem(bc.getNome());
        }
    }
    
