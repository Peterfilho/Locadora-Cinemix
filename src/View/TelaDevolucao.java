    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String [] {"ID","Cliente","Locação", "Devolução","Status", "Total"};
        conexao.conexao();
            conexao.executaSql(Sql);
            try{
               conexao.rs.first();
               do{
                   dados.add(new Object[]{conexao.rs.getString("id_locacao"), conexao.rs.getString("nome"), conexao.rs.getString("data_locacao"), conexao.rs.getDate("data_devolucao"), conexao.rs.getString("status"), conexao.rs.getString("valor_total")});
                   
               }while(conexao.rs.next());
            }catch (SQLException ex){
                JOptionPane.showMessageDialog(rootPane,"Não há devoluções para a data de hoje!");
            }
            ModelBeans.BeansTabela modelo = new ModelBeans.BeansTabela(dados, colunas);
            jTableDevolucoes.setModel(modelo);
            //IDLOCACAO 
            jTableDevolucoes.getColumnModel().getColumn(0).setPreferredWidth(30);
            jTableDevolucoes.getColumnModel().getColumn(0).setResizable(false);
            //CLIENTE 
            jTableDevolucoes.getColumnModel().getColumn(1).setPreferredWidth(220);
            jTableDevolucoes.getColumnModel().getColumn(1).setResizable(false);
            //DATA LOCACAO
            jTableDevolucoes.getColumnModel().getColumn(2).setPreferredWidth(100);
            jTableDevolucoes.getColumnModel().getColumn(2).setResizable(false);
            //DATA DEVOLUCAO
            jTableDevolucoes.getColumnModel().getColumn(3).setPreferredWidth(100);
            jTableDevolucoes.getColumnModel().getColumn(3).setResizable(false);
            //STATUS
            jTableDevolucoes.getColumnModel().getColumn(4).setPreferredWidth(80);
            jTableDevolucoes.getColumnModel().getColumn(4).setResizable(false);
            //TOTAL
            jTableDevolucoes.getColumnModel().getColumn(5).setPreferredWidth(63);
            jTableDevolucoes.getColumnModel().getColumn(5).setResizable(false);
            //NAO PODE REORDENAR O CABEÇALHO DA TABELA
            jTableDevolucoes.getTableHeader().setReorderingAllowed(false);
            //NAO PODE SER REDIMENCIONADA 
            jTableDevolucoes.setAutoResizeMode(jTableDevolucoes.AUTO_RESIZE_OFF);
            //SO PODERA SELECIONAR UM DADO NA TABELA DE CADA VEZ 
            jTableDevolucoes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conexao.desconecta();
    }
    
