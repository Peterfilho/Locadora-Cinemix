    private void jButtonReceberFilmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReceberFilmeActionPerformed
        BeansDevolucao dev = new BeansDevolucao();
       
        String idlocacao =""+jTableDevolucoes.getValueAt(jTableDevolucoes.getSelectedRow(),0);
        //JOptionPane.showMessageDialog(rootPane, idlocacao);
        conexao.conexao();
            conexao.executaSql("SELECT * from locacao join cliente using (id_cliente) where id_locacao = "+idlocacao+"");
        try {
            if(conexao.rs.first()){
                dev.setIdCliente(conexao.rs.getInt("id_cliente"));
                dev.setIdLocacao(conexao.rs.getInt("id_locacao"));
                dev.setDataDevolucao(conexao.rs.getDate("data_devolucao"));
                dev.setDataLocacao(conexao.rs.getDate("data_locacao"));
                dev.setStatus(conexao.rs.getString("status"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar locacao "+ex);
            ex.printStackTrace();
        }    
        
        dao.devolver(dev);
        
        conexao.desconecta();
        
        preencherTabela("SELECT concat( primeiro_nome, ' ', sobrenome ) AS 'nome',id_locacao, data_locacao, data_devolucao, status, valor_total FROM locacao JOIN cliente USING ( id_cliente ) WHERE data_devolucao = curdate( ) and status like '%em aberto%'");
    }//GEN-LAST:event_jButtonReceberFilmeActionPerformed

