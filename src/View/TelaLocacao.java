    public void somarTotal(){
        conexao.conexao();
            conexao.executaSql("SELECT CONCAT( primeiro_nome, ' ', sobrenome) as 'nome' FROM cliente order by primeiro_nome");
            try {
                conexao.rs.first();
                do{
                    jComboBoxClientes.addItem(conexao.rs.getString("nome"));
                }while(conexao.rs.next());
            } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(null, "Erro ao preencher Clientes \n" + ex.getMessage());
            }
        conexao.desconecta();
    }
    
    public void preencherClientes(){
        
        //DaoCadCliente dao = new DaoCadCliente();
        //clientes = dao.all();
        
        conexao.conexao();
            conexao.executaSql("SELECT CONCAT( primeiro_nome, ' ', sobrenome) as 'nome', id_cliente FROM cliente order by primeiro_nome");
            try {
                conexao.rs.first();
                jComboBoxClientes.removeAllItems();
                do{
                    jComboBoxClientes.addItem(conexao.rs.getString("nome"));
                    
                    BeansCadCliente c = new BeansCadCliente();
                    c.setId(conexao.rs.getInt("id_cliente"));
                    
                    clientes.add( c );
                    
                }while(conexao.rs.next());
            } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(null, "Erro ao preencher Clientes \n" + ex.getMessage());
            }
        conexao.desconecta();
    }
    
