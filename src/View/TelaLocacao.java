    private void jButtonRealizarLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRealizarLocacaoActionPerformed
         BeansLocacao locacao = new BeansLocacao();
         // CLIENTE QUE ESTA REALIZANDO LOCACAO
         locacao.setCliente_locacao(jComboBoxClientes.getSelectedItem().toString());
         // DATA DA LOCAÇAO
         java.sql.Date dataLocacao = new java.sql.Date( jDateChooserLocacao.getDate().getTime() );
         locacao.setData_locacao(dataLocacao);
         // DATA DA DEVOLUÇAO
         java.sql.Date dataDevolucao = new java.sql.Date( jDateChooserDevolucao.getDate().getTime() );
         locacao.setData_devolucao(dataDevolucao);
         //JOptionPane.showMessageDialog(rootPane, dataDevolucao);
         // VALOR TOTAL
         locacao.setTotal(jTextFieldTotal.getText());
         // FILMES QUE ESTAO NA TABELA
      
         locacao.setId_cliente( clientes.get( jComboBoxClientes.getSelectedIndex() ).getId() );
         
         DaoLocacao dao = new DaoLocacao();
         int idLocacao = dao.salvarlocacao(locacao);
         
         
         //String idFilme =""+jTableFilmes.getValueAt(jTableFilmes.getSelectedRow(),0);
         //JOptionPane.showMessageDialog(rootPane, idFilme);
         
         for (int i = 0; i < jTableLocacao.getRowCount(); i++) {
            int idItemLoc = Integer.parseInt(""+jTableLocacao.getValueAt(i, 0));
            dao.salvarItensLocacao(idLocacao,idItemLoc);
        }
        // MENSAGEM DE LOCAÇÃO REALIZADA COM SUCESSO!
         JOptionPane.showMessageDialog(rootPane,"Locação realizada com sucesso!");
        // INSTANCIANDO TELA NOVAMENTE PARA LIMPAR OS CAMPOS LIKE A MCGAIVER
         TelaLocacao tela = new TelaLocacao();
         tela.setVisible(true);
         dispose();
    }//GEN-LAST:event_jButtonRealizarLocacaoActionPerformed

